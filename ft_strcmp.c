int	ft_strcmp(const char *s1, const char *s2)
{	
	int	i;
	int	j;
	int	res;

	i = 0;
	j = 0;
	while (s1[i] == s2[j] && s1[i] != '\0' && s2[j] != '\0')
	{	
		i++;
		j++;
	}
	res = s1[i] - s2[j];
	return (res);
}
