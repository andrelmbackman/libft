int	ft_tolower(int c)
{
	unsigned char	ch;

	ch = (unsigned char)c;
	if ((ch + 32) >= 'a' && (ch + 32) <= 'z')
	{
		ch = ch + 32;
		return (ch);
	}
	else
	{
		return (c);
	}
}
