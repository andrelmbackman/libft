int	ft_toupper(int c)
{
	unsigned char	ch;

	ch = (unsigned char)c;
	if((ch - 32) >= 'A' && (ch - 32) <= 'Z')
	{
		ch = ch - 32;
		return (ch);
	}
	else
	{
		return (c);
	}
}
