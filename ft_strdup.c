#include "libft.h"

char	*ft_strdup(char *src)
{
	size_t	i;
	char	*str;

	i = ft_strlen(src);
	str = (char *)malloc(i * sizeof(char));
	i = 0;
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	free(str);
	return (str);
}
